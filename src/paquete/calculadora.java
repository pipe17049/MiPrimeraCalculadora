    private void Boton_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_igualActionPerformed
        //Obtenet texto de la casilla
        this.numero2=Float.parseFloat(this.casilla.getText());
        // TODO add your handling code here:
        
        //Switch para saber el caso de la operacion
        switch(this.operador){

            case "+":this.casilla.setText(eliminardeci(this.numero1 + this.numero2));break;
            case "-":this.casilla.setText(eliminardeci(this.numero1 - this.numero2));break;
            case "*":this.casilla.setText(eliminardeci(this.numero1 * this.numero2));break;
            case "/":
            if(this.numero2==0){
                this.casilla.setText("No division entre 0");
            }
            else{
                this.casilla.setText(eliminardeci(this.numero1 / this.numero2));
            }

            break;

        }
        //Bandera que se usa para poder borrar pantalla despues de una operacion
        this.bandera=true;
    }//GEN-LAST:event_Boton_igualActionPerformed

