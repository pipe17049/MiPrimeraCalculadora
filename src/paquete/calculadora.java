    //Convertiir Flotante a String
    public String eliminardeci(float resultado){ 
        String retorno="";
        retorno = Float.toString(resultado);
        //SI termina en .0, elimina el .0 de pantalla
        if(resultado%1==0){
            retorno=retorno.substring(0,retorno.length()-2);
        }
        
        return retorno;
    }
